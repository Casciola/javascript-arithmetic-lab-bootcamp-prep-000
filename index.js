function add(a,b) {
  return a + b;
}

function substract(a,b) {
  return a - b;
}

function multiply(a,b) {
  return a * b;
}

function divide(a,b) {
  return a / b;
}

function inc(n) {
   a = n + 1;
   return a;
}

function dec(n) {
  a = n - 1;
  return a;
}

function makeInt(n) {
  return parseInt(n);
}
